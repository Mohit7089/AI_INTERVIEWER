import re

def extract_projects(text):

    lines = [l.strip() for l in text.split('\n') if l.strip()]

    X = vectorizer_lines.transform(lines)
    preds = model_lines.predict(X)

    projects = []
    current_project = None
    in_project_section = False

    for line, label in zip(lines, preds):

        clean = re.sub(r'\s+', ' ', line)
        lower = clean.lower()

        # 🔥 detect project section (robust)
        if "project" in lower:
            in_project_section = True

        # 🔥 stop at major sections
        if any(x in lower for x in ["education", "technical skills", "skills"]):
            in_project_section = False

        # ❌ skip if not in project section
        if not in_project_section:
            continue

        # 🔥 FIX: REMOVE "projects" word from line
        clean = re.sub(r'(?i)projects?', '', clean).strip()
        lower = clean.lower()

        # 🔥 TITLE DETECTION (RELAXED)
        if (
            label == "TITLE" and
            2 <= len(clean.split()) <= 8 and
            not clean.endswith('.') and
            not clean.isupper() and
            not any(x in lower for x in [
                "email", "phone", "linkedin", "github"
            ])
        ):
            current_project = {
                "project_name": clean,
                "summary": ""
            }
            projects.append(current_project)
            continue

        # 🔥 DESCRIPTION
        if current_project:
            if len(clean.split()) > 5:
                current_project["summary"] += " " + clean

    # 🔥 CLEAN OUTPUT
    final = []
    for p in projects:
        if len(p["summary"].split()) > 5:
            final.append({
                "project_name": p["project_name"],
                "summary": " ".join(p["summary"].split()[:25]) + "..."
            })

    return final[:5]

