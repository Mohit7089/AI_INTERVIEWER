def extract_soft_skills(text):

    soft_skills_db = [
        "communication", "leadership", "teamwork", "collaboration",
        "problem solving", "critical thinking", "adaptability",
        "time management", "creativity", "decision making",
        "analytical thinking", "interpersonal skills"
    ]

    text = text.lower()

    return [skill for skill in soft_skills_db if skill in text]


def detect_soft_skills_from_actions(text):

    text = text.lower()

    mapping = {
        "lead": "leadership",
        "managed": "leadership",
        "team": "teamwork",
        "collaborated": "teamwork",
        "worked with": "teamwork",
        "solved": "problem solving",
        "improved": "analytical thinking",
        "organized": "management",
        "communicated": "communication"
    }

    found = set()

    for word, skill in mapping.items():
        if word in text:
            found.add(skill)

    return list(found)


def extract_all_soft_skills(text):

    direct = extract_soft_skills(text)
    inferred = detect_soft_skills_from_actions(text)

    return list(set(direct + inferred))

import pdfplumber

def extract_text_from_pdf(file):
    text = ""

    with pdfplumber.open(file) as pdf:
        for page in pdf.pages:

            # 🔥 preserve layout (THIS IS KEY)
            t = page.extract_text(x_tolerance=2, y_tolerance=2)

            if t:
                text += t + "\n"

    return text





