def label_line(line):
    lower = line.lower()

    if any(w in lower for w in ["developed","built","created","designed","implemented"]):
        return "DESCRIPTION"
    elif len(line.split()) <= 6 and not line.endswith('.'):
        return "TITLE"
    elif any(w in lower for w in ["java","python","sql","react","node"]):
        return "SKILLS"
    else:
        return "OTHER"

