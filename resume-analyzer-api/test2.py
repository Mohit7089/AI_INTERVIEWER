def adjust_roles_soft(skills, roles):

    skills = [s.lower() for s in skills]
    role_dict = dict(roles)

    # 🔥 SIMPLE CONDITION (no complex logic)
    if "react" in skills and "node" in skills:
        role_dict["Full Stack Developer"] = role_dict.get("Full Stack Developer", 0) + 0.6

    if "python" in skills:
        role_dict["Python Developer"] = role_dict.get("Python Developer", 0) + 0.3

    if "java" in skills:
        role_dict["Java Developer"] = role_dict.get("Java Developer", 0) + 0.3

    if "scikit-learn" in skills:
        role_dict["Data Science"] = role_dict.get("Data Science", 0) + 0.3

    total = sum(role_dict.values())
    role_dict = {k: v / total for k, v in role_dict.items()}

    return sorted(role_dict.items(), key=lambda x: x[1], reverse=True)[:3]

def predict_roles(text):
    cleaned = clean_text(text)
    vec = vectorizer.transform([cleaned])

    probs = model.predict_proba(vec)[0]

    top_roles = sorted(
        zip(le.classes_, probs),
        key=lambda x: x[1],
        reverse=True
    )[:3]

    # Normalize confidence (optional)
    total = sum([p for _, p in top_roles])
    top_roles = [(r, float(p/total)) for r, p in top_roles]

    return top_roles

