def calculate_score(skills, projects, text):
    score = 0

    score += min(len(skills) * 4, 30)
    score += min(len(projects) * 10, 30)

    if "experience" in text.lower():
        score += 20

    if any(word in text.lower() for word in ["btech", "mca", "degree"]):
        score += 10

    return min(score, 100)

