def extract_skills(text):

    text=text.lower()

    result={}
    all_skills=[]

    for category,skills in TECHNICAL_SKILLS.items():

        found=[]

        for skill in skills:

            pattern=r"\b"+re.escape(skill.lower())+r"\b"

            if re.search(pattern,text):

                found.append(skill)
                all_skills.append(skill)

        result[category]=sorted(found)

    return {
        "categorized":result,
        "all":sorted(set(all_skills))
    }

def calculate_score(skills, text):

    if isinstance(skills, dict):
        skills = skills["all"]

    score = 0
    lower = text.lower()

    # Technical Skills (40)
    score += min(len(skills) * 3, 40)

    # Education (15)
    if any(x in lower for x in [
        "btech",
        "bachelor",
        "mca",
        "master",
        "degree"
    ]):
        score += 15

    # Experience (20)
    if "present" in lower or "experience" in lower:
        score += 20

    # Projects (15)
    if any(x in lower for x in [
        "project",
        "developed",
        "built",
        "created"
    ]):
        score += 15

    # Contact (10)
    if re.search(r"[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}", text):
        score += 5

    if re.search(r"\+?\d[\d\s\-]{8,15}", text):
        score += 5

    return min(score, 100)

def extract_contact_info(text):

    email = re.search(
        r"[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}",
        text
    )

    phone = re.search(
        r"\+?\d[\d\s\-]{8,15}",
        text
    )

    linkedin = re.search(
        r"https?://(?:www\.)?linkedin\.com/[^\s]+",
        text,
        re.I
    )

    github = re.search(
        r"https?://(?:www\.)?github\.com/[^\s]+",
        text,
        re.I
    )

    return {
        "email": email.group(0) if email else "Not Found",
        "phone": phone.group(0) if phone else "Not Found",
        "linkedin": linkedin.group(0) if linkedin else "Not Found",
        "github": github.group(0) if github else "Not Found"
    }

def extract_education(text):

    degrees=[]

    lower=text.lower()

    for degree in [
        "btech",
        "mca",
        "bachelor",
        "master",
        "bsc",
        "msc"
    ]:

        if degree in lower:

            degrees.append(degree.upper())

    return degrees


def extract_experience(text):
    lower = text.lower()

    if "present" in lower:
        return "Experienced"

    match = re.search(
        r"\d+\+?\s*(years?|months?)",
        lower
    )
    if match:
        return match.group()

    ranges = re.findall(
        r"(jan|feb|mar|apr|may|jun|jul|aug|sep|oct|nov|dec)[a-z]*\s+\d{4}",
        lower
    )

    if ranges:
        return f"{len(ranges)} work period(s) found"

    if "fresher" in lower:
        return "Fresher"

    return "Not Mentioned"

