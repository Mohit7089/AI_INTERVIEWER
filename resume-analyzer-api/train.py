import pdfplumber

def extract_text_from_pdf(file):
    text = ""

    with pdfplumber.open(file) as pdf:
        for page in pdf.pages:

            # 🔥 preserve layout (THIS IS KEY)
            t = page.extract_text(x_tolerance=2, y_tolerance=2)

            if t:
                text += t + "\n"

    return text





